# მიიღებს 2 რიცხვს
# რიცხვი 1 არის თუ არა რიცხვი 2-ის ჯერადი
# ჯერადობას დაბეჭდავს ეკრანზე
# ჯერადობა --> A = B * n

number_1 = int(input("დაწერე პირველი რიცხვი "))
number_2 = int(input("დაწერე მეორე რიცხვი "))

number_3 = number_1 / number_2

if number_3 == int(number_1 / number_2):
    print("პირველი რიცხვი მეორის ჯერადია")
else:
    print("პირველი რიცხვი არ არის მეორის ჯერადი")